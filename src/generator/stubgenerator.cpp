QByteArray StubGenerator::encryptData(const QByteArray& data, const QByteArray& key) {
    QByteArray encrypted = data;
    for (int i = 0; i < encrypted.size(); i++) {
        encrypted[i] = encrypted[i] ^ key[i % key.size()];
    }
    return encrypted;
}

